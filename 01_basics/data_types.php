<?php
// PHP supports various data types: strings, integers, floats, booleans, arrays, and objects.

// String
$string = "Hello, PHP!";

// Integer
$integer = 42;

// Float
$float = 3.14;

// Boolean
$boolean = true;

// Display data types using gettype()
echo "Data Type of \$string: " . gettype($string) . "<br>";
echo "Data Type of \$integer: " . gettype($integer) . "<br>";
echo "Data Type of \$float: " . gettype($float) . "<br>";
echo "Data Type of \$boolean: " . gettype($boolean) . "<br>";
?>
